const answer1Div = document.getElementById("answer-1");
const btn1 = document.getElementById("question-1");

btn1.onclick = function () {
  if (answer1Div.style.display == "block") {
    answer1Div.style.display = "none";
  } else {
    answer1Div.style.display = "block";
  }
};

